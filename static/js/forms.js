function loadingScreen(){
    $('#loading-bg').show();
    $('#loading-image-nobg').show();
    $("title").text("Carregando | " + $("title").text());
}

function endLoadingScreen(){
    $('#loading-bg').hide();
    $('#loading-image-nobg').hide();
    if ($("title").text().indexOf("Carregando | ") !== -1){
        $("title").text($("title").text().split("Carregando | ")[1]);
    }
};

